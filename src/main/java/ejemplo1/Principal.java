    private void editarpuntosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarpuntosActionPerformed
        // TODO add your handling code here:
        if (getPuntoSeleccionado() != null)
        {
            getPuntoSeleccionado().setPx(Integer.parseInt(puntoX.getText()));
            getPuntoSeleccionado().setPy(Integer.parseInt(puntoY.getText()));
        }

        listapuntos.updateUI();
    }//GEN-LAST:event_editarpuntosActionPerformed

    private void agregarpuntoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarpuntoActionPerformed
        // TODO add your handling code here:
        if (FiguraSeleccionada != null)
        {

            int px = Ints.tryParse(puntoX.getText());
            int py = Ints.tryParse(puntoY.getText());

            puntoX.setText("" + px);
            puntoY.setText("" + py);

            FiguraSeleccionada.getListaPuntos().addElement(new puntos(px, py));
        }
    }//GEN-LAST:event_agregarpuntoActionPerformed

